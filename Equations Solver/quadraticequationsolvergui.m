% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
vala=str2double(get(handles.a, 'string'));
valb=str2double(get(handles.b, 'string'));
valc=str2double(get(handles.c, 'string'));
d=(valb^2)-4*vala*valc;
if d<0
    msgbox('complex number','Error')
elseif d>=0
    x1=(-valb + d^(1/2))/2*vala;
x2=(-valb - d^(1/2))/2*vala;
set(handles.x1, 'string', x1)
set(handles.x2, 'string', x2)
    msgbox('normal')
ts=str2double(get(handles.x1, 'String'));
set(handles.edit12, 'String', ts);

cd=str2double(get(handles.x2, 'String'));
set(handles.edit14, 'String', cd)

end


